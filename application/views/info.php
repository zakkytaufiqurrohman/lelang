	<h3 class="step-title">Kayu Jati</h3>
    <div class="step-desc"><ul>
    	<p>Jati (<em>Tectona grandis</em> L.f) merupakan tanaman yang termasuk kedalam kelas <em>Verbenacea</em>.</p>
<p><strong>Ciri Umum :</strong></p>
<p><span style="text-decoration: underline;"><strong>Warna</strong></span> : teras berwarna kuning emas kecoklatan sampai coklat kemerahan, mudah dibedakan dari gubal yang berwarna putih agak ke abu abuan. <span style="text-decoration: underline;"><strong>Corak:</strong></span> Dekoratif yang indah berkat jelasnya lingkaran tumbuh, sedikit buram dan berminyak. <span style="text-decoration: underline;"><strong>Tekstur:</strong></span> agak kasar sampai kasar dan tidak rata. <span style="text-decoration: underline;"><strong>Arah Serat:</strong></span> Lurus, Begelombang sampai agak berpadu. <span style="text-decoration: underline;"><strong>Lingkaran Tumbuh:</strong></span> tampak sangat jelas, baik pada bidang melintang, radial maupun tangensial. <span style="text-decoration: underline;"><strong>Kekerasan:</strong></span> Agak Keras.</p>
<p><strong>Ciri Anatomi:</strong></p>
<p><span style="text-decoration: underline;"><strong>Pembuluh/Pori :</strong></span> tata lingkar, bentuk bundar sampai bundar telur, diameter tangensial bagian kayu awal sekitar 340-370 mikron, pada kayu akhirya sekitar 50-290 mikron, bidang perforasi sederhana, berisi tilosis atau endapan berwarna putih. <span style="text-decoration: underline;"><strong>Parenkima:</strong></span> yang bertipe paratrakea: bentuk selubung tipis, pada bagian kayu awal selubung itu agak lebar sampai membentuk pita marginal; yang bertipe apotrakea jarang, umumnya membentuk rantai yang terdiri atas sekitar 4 sel. <span style="text-decoration: underline;"><strong>Jari-Jari</strong></span>: lebar, terdiri atas 4 seri atau lebih, jumlahnya sekitar 4-7 per mm, arahnya tangensial, komposisi selnya homoselular (hanya sel-sel baring), tingginya dapat mencapai 0.9 mm</p>
<p><strong>Sifat dan Kegunaan:</strong></p>
<p><span style="text-decoration: underline;">Berat Jenis:</span> rata-rata 0,67 (0,62 - 0,75); <span style="text-decoration: underline;"><strong>Kelas awet</strong></span>: I-II; <span style="text-decoration: underline;"><strong>Kelas Kuat</strong></span>: II; <span style="text-decoration: underline;"><strong>Kegunaan:</strong></span> dipakai untuk berbagai keperluan, antara lain bahan bangunan, rangka pintu dan jendela, panel pintu, bantalan kereta api, perabot rumah tangga, karoseri badan truk, dek kapal, lumber sering, vinir indah. Kayu Jati juga sering digunakan sebagai bahan dasar furniture dan industri kayu baik di dalam negeri maupun mancanegara.</p>
<p><strong>Nama Lain:</strong></p>
<p>Deleg, dodolan, jate, jateh, jatih, jatos, kulidawa</p>
<p>kayu ini juga merupakan salah satu jenis pohon yang tingkat pertumbuhannya lambat dan rendah, sehingga membutuhkan waktu yang cukup lama untuk menunggu pohon jati hingga dapat dimanfaatkan kayunya. Pohon ini merupakan salah satu tanaman&nbsp;yang menghasilkan kayu dengan kualitas yang&nbsp;luar biasa dan sudah terkenal di seluruh dunia. Banyak sekali barang-barang yang terbuat dari kayu jati memiliki daya tahan yang sangat lama.</p>
